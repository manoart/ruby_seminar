x, y = y, x      # Werte vertauschen
a = [1, 2, 3, 4] # <=> a = 1, 2, 3, 4
b, c  = a        # => b == 1,  c == 2
b, *c = a        # => b == 1,  c == [2, 3, 4]
b, c  = 42, a    # => b == 42, c == [1, 2, 3, 4]
b, *c = 42, a    # => b == 42, c == [[1, 2, 3, 4]]
b, c  = 42, *a   # => b == 42, c == 1
b, *c = 42, *a   # => b == 42, c == [1, 2, 3, 4]
